//data types in java-:

//byte [-128 : 127] - 8 bits
//short [-32768 : 32767] - 16 bits
//int [-2,147,483,648 : 2,147,483, 647]- 32 bits
//long [-9,223,372,036,854,775,808 : 9,223,372,036,854,775,807]- 64 bits
//float [1.40239846*10^-45 : 3.40282347*10^38] - 32 bits
//double [4.940656458124654*10^-324 : 1.7976931348623157*10^3081]- 64 bits


public class day1 {
    public static void main(String[] args) {
        char c = '=';
        System.out.println(c);

        boolean bool = true;
        System.out.println(bool);

        byte b = 2;
        System.out.println(b);

        int i = 10;
        System.out.println(i);

        long l = 20;
        System.out.println(l);

        short s = 5;
        System.out.println(s);

        float f = -3.4F;
        System.out.println(f);


    }
}